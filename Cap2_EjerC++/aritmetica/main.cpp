#include <iostream>

using namespace std;

int main()
{
    int x,y;
    cout << "Ingrese primer numero: " << endl;
    cin >> x;
    cout << "Ingrese segundo numero: " << endl;
    cin >> y;
    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " * " << y << " = " << x * y << endl;
    cout << x << " - " << y << " = " << x - y << endl;
    cout << x << " / " << y << " = " << x / y << endl;

    return 0;
}
